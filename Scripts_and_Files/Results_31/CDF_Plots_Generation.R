################################################################################################################
################################################################################################################
#### 4. DESCRIPTION: The script read the results obtained in LOOCV script, where ability to rank genes of RWR
####                 is tested in monoplex and multiplex situations. With this data we generate a CDF and
####                 we display it focused in the first ranks (The method is focused on prioritization. Therefore
####                 that is the interesting part of the plot)
####       
################################################################################################################
################################################################################################################
#### 5. OUTPUT: A plot showing the different CDFs for the different multiplex and monoplex approaches.
################################################################################################################
################################################################################################################
#### 6. EXAMPLE OF EXECUTION: Just Run it. Rscript CDF_Plots_Generation.R
####                          
################################################################################################################
################################################################################################################
rm(list=ls())

################################################################################################################
## 1.- We load the common network generation functions, and we install/load the packages needed. 
################################################################################################################
source("LOOCV_Utils.R")
CRAN.packages <- c("reshape2","plyr","ggplot2")
bioconductor.packages <- c()
install.packages.if.necessary(CRAN.packages, bioconductor.packages)

# We create a directoy called networks_files to save the files generated.
data.dir <- "Results_31"
if (!file.exists(data.dir)) {
  dir.create(data.dir)
}

################################################################################################################
## 2.- We read the ranking results generated by the RWR algorithm in the different situations.
################################################################################################################

Input_Files_PPI <- read.table("Results_31/PPI.txt", header = TRUE, stringsAsFactors = FALSE)
# > mean(Input_Files_PPI$Global_Ranking)
# [1] 1341.937
Input_Files_PATH <- read.table("Results_31/Pathways.txt", header = TRUE, stringsAsFactors = FALSE)
# > mean(Input_Files_PATH$Global_Ranking)
# [1] 699.2332
Input_Files_COEX <- read.table("Results_31/Co-expression.txt", header = TRUE, stringsAsFactors = FALSE)
# > mean(Input_Files_COEX$Global_Ranking)
# [1] 1797.696
Input_Files_PPI_COEX <- read.table("Results_31/PPI_Co-expression.txt", header = TRUE, stringsAsFactors = FALSE)
# > mean(Input_Files_PPI_COEX$Global_Ranking)
# [1] 1229.652
Input_Files_PPI_PATH <- read.table("Results_31/PPI_Pathways.txt", header = TRUE, stringsAsFactors = FALSE)
# > mean(Input_Files_PPI_PATH$Global_Ranking)
# [1] 743.7264
Input_Files_PATH_COEX <- read.table("Results_31/Pathways_Coex.txt", header = TRUE, stringsAsFactors = FALSE)
# > mean(Input_Files_PATH_COEX$Global_Ranking)
# [1] 856.6021
Input_Files_PPI_PATH_COEX <- read.table("Results_31/PPI_Pathways_Coex.txt", header = TRUE, stringsAsFactors = FALSE)
# > mean(Input_Files_PPI_PATH_COEX$Global_Ranking)
# [1] 802.3072
  
################################################################################################################
## 3.- We generate a data frame with all the rankings we are going to take in account. We calculate CDF
################################################################################################################
PPI <- Input_Files_PPI$Global_Ranking
PATH <- Input_Files_PATH$Global_Ranking
COEX <- Input_Files_COEX$Global_Ranking
PPI_COEX <- Input_Files_PPI_COEX$Global_Ranking
PPI_PATH <- Input_Files_PPI_PATH$Global_Ranking
PATH_COEX <- Input_Files_PATH_COEX$Global_Ranking
PPI_PATH_COEX <- Input_Files_PPI_PATH_COEX$Global_Ranking

ggdata <- data.frame(PPI,PATH,COEX,PPI_COEX,PPI_PATH,PATH_COEX,PPI_PATH_COEX)

ggdata <- melt(ggdata)
ggdata <- ddply(ggdata, .(variable), transform, ecd=ecdf(value)(value))

################################################################################################################
## 4.- We generate a data frame with all the rankings we are going to take in account. We calculate CDF
################################################################################################################
cdf <- ggplot(ggdata, aes(x=value)) + stat_ecdf(aes(colour=variable,linetype=variable),size = 1.75) +xlab("Rank") + 
              ylab("Cumulative Distribution") +theme_bw() +   theme(axis.text=element_text(size=16),
              axis.title=element_text(size=18,face="bold"), legend.title=element_blank(),
              legend.text = element_text(size = 12, face = "bold"), 
              legend.position=c(0.15,0.8), legend.key.size = unit(1.2, "cm"), 
              legend.background = element_rect(colour='black',size=0.5),
              panel.grid.major = element_line(size = 0.5, colour = 'black',linetype='dotted'),  
              panel.border=element_rect(size=0.75,colour='black')) 
# legend.background = element_blank()
cdf

cdf + coord_cartesian(xlim = c(1, 60), ylim = c(0,0.35)) 
ggsave("../Figures_V2/Figure_2.jpg")


